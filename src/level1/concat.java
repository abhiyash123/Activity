package level1;

import java.util.Scanner;

/**
 *
 * @author 91780
 */
public class concat {
    
    
    public static void main(String[] args) {
        
        
         Scanner sc = new Scanner(System.in);
        
        String a = sc.nextLine();
        String b = sc.nextLine();
        String c;
        
        
        c = a.concat(b);
        
        System.out.println(c);
        
        
        
    }
    
}
